package com.patternPractise;

public class AlphabetL {
	public static void alphabetL()
	{
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=5;j++)
			{
				{if(j==1||i==5)
					System.out.print("*");
					
				}
				
				
			}
			System.out.println();
		}
	}
	public static void main(String[] args) {
		alphabetL();
		
	}

}
